package com.example.gadsleaderboardmobile.Model;

public class SubmissionModel {
    String fistName;
    String lastName;
    String emailAddress;
    String githubLink;

    public SubmissionModel(String fistName, String lastName, String emailAddress, String githubLink) {
        this.fistName = fistName;
        this.lastName = lastName;
        this.emailAddress = emailAddress;
        this.githubLink = githubLink;
    }

    public String getFistName() {
        return fistName + ".1824927963";
    }

    public String getLastName() {
        return lastName + ".1877115667";
    }

    public String getEmailAddress() {
        return emailAddress + ".2006916086";
    }

    public String getGithubLink() {
        return githubLink + ".284483984";
    }
}
